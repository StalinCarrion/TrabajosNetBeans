public class DFuncionalidad_Rol {
     private int intRol_id;
     private int intFun_id;

    public DFuncionalidad_Rol() {
    }

    public DFuncionalidad_Rol(int intRol_id, int intFun_id) {
        this.intRol_id = intRol_id;
        this.intFun_id = intFun_id;
    }

    public int getIntRol_id() {
        return intRol_id;
    }

    public void setIntRol_id(int intRol_id) {
        this.intRol_id = intRol_id;
    }

    public int getIntFun_id() {
        return intFun_id;
    }

    public void setIntFun_id(int intFun_id) {
        this.intFun_id = intFun_id;
    }
     
    
}
