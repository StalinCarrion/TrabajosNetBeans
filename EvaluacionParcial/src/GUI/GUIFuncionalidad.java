    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if (txtFun_descripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion");
            txtFun_descripcion.requestFocus();
            return;
        }

        DFuncionalidad dts = new DFuncionalidad();
        fFuncionalidad func = new fFuncionalidad();

        dts.setStrFun_descripcion(txtFun_descripcion.getText());

        int seleccionado = cboFun_Nombre.getSelectedIndex();
        dts.setStrFun_nombre((String) cboFun_Nombre.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La funcion fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIntFun_id(Integer.parseInt(txtId_fun.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La funcion fue Editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtId_fun.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est√°s seguro de Eliminar la Funcion?", "Confirmar", 2);

            if (confirmacion == 0) {
                fFuncionalidad func = new fFuncionalidad();
                DFuncionalidad dts = new DFuncionalidad();

                dts.setIntFun_id(Integer.parseInt(txtId_fun.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jTableFuncionalidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionalidadMouseClicked
        // TODO add your handling code here:
        btnInsertar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = jTableFuncionalidad.rowAtPoint(evt.getPoint());

        txtId_fun.setText(jTableFuncionalidad.getValueAt(fila, 0).toString());
        txtFun_descripcion.setText(jTableFuncionalidad.getValueAt(fila, 1).toString());

        cboFun_Nombre.setSelectedItem(jTableFuncionalidad.getValueAt(fila, 2).toString());
    }//GEN-LAST:event_jTableFuncionalidadMouseClicked

