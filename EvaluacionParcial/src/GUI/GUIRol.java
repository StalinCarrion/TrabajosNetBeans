    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion");
            txtDescripcion.requestFocus();
            return;
        }

        DRol dts = new DRol();
        fRol func = new fRol();

        dts.setStrRol_descripcion(txtDescripcion.getText());

        int seleccionado = cboRolNombre.getSelectedIndex();
        dts.setStrRol_nombre((String) cboRolNombre.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El rol fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIntRol_id(Integer.parseInt(txtIdRol.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El rol fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

