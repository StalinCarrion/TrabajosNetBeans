    void mostrar(String buscar) {
        try {
            DefaultTableModel modelo;
            fRol func = new fRol();
            modelo = func.mostrar(buscar);

            jTableRol.setModel(modelo);
            ocultar_columnas();

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }

