    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        DFuncionalidad_Rol dtsRF = new DFuncionalidad_Rol();
        fRol_Funcionalidad funRF = new fRol_Funcionalidad();

        dtsRF.setIntFun_id(Integer.parseInt(txtId_fun.getText()));
        dtsRF.setIntRol_id(Integer.parseInt(txtIdRol.getText()));

        if (accion.equals("guardar")) {
            if (funRF.insertar(dtsRF)) {
                JOptionPane.showMessageDialog(rootPane, "El rol Funcionalidad fue registrado satisfactoriamente");
                mostrarROL_Funcionalidad("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dtsRF.setIntRol_id(Integer.parseInt(txtIdRol.getText()));
            dtsRF.setIntFun_id(Integer.parseInt(txtId_fun.getText()));
            if (funRF.editar(dtsRF)) {
                JOptionPane.showMessageDialog(rootPane, "El rol Funcionalidad fue Editado satisfactoriamente");
                mostrarROL_Funcionalidad("");
                inhabilitar();
            }
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

