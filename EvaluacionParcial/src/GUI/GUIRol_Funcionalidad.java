    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtIdRol.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estás seguro de Eliminar el Rol- Funcionalidad?", "Confirmar", 2);

            if (confirmacion == 0) {

                DFuncionalidad_Rol dtsRF = new DFuncionalidad_Rol();
                fRol_Funcionalidad funRF = new fRol_Funcionalidad();

                dtsRF.setIntRol_id(Integer.parseInt(txtIdRol.getText()));
                funRF.eliminar(dtsRF);
                mostrarROL_Funcionalidad("");
                inhabilitar();

            }

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

