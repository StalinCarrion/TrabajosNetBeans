    void mostrar(String buscar) {
        try {
            DefaultTableModel modeloR;
            DefaultTableModel modeloF;

            fRol funcR = new fRol();
            fFuncionalidad funcF = new fFuncionalidad();

            modeloR = funcR.mostrar(buscar);
            modeloF = funcF.mostrar(buscar);

            jTableRol.setModel(modeloR);
            jTableFuncionalidad.setModel(modeloF);

            //ocultar_columnas();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }

