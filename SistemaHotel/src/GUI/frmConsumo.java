    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtIdConsumo.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est√°s seguro de Eliminar el Producto?", "Confirmar", 2);

            if (confirmacion == 0) {
                fConsumo func = new fConsumo();
                vConsumo dts = new vConsumo();

                dts.setIntIdconsumo(Integer.parseInt(txtIdConsumo.getText()));
                func.eliminar(dts);
                mostrar(idreserva);
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

