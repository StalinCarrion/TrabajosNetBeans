    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Control de errores

        if (txtIdHabitacion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar una Habitacion.");
            txtIdHabitacion.requestFocus();
            return;
        }
        if (txtIdCliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar un Cliente");
            txtIdCliente.requestFocus();
            return;
        }
        if (txtCost_alojamiento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio a tu Reserva");
            txtCost_alojamiento.requestFocus();
            return;
        }

        vReserva dts = new vReserva();
        //LLamar a todas las funciones
        fReserva func = new fReserva();

        dts.setIntIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
        dts.setIntIdCliente(Integer.parseInt(txtIdCliente.getText()));
        dts.setIntIdTrabajador(intIdUsuario);

        int seleccionado = cboTipo_reserva.getSelectedIndex();
        dts.setStrTipo_reserva((String) cboTipo_reserva.getItemAt(seleccionado));

        Calendar cal;
        int d, m, a;
        cal = dcFecha_Reserva.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setDateFecha_reserva(new Date(a, m, d));

        cal = dcFecha_ingreso.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setDateFecha_ingresa(new Date(a, m, d));

        cal = dcFecha_salida.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setDateFecha_salida(new Date(a, m, d));

        dts.setDbCosto_alojamiento(Double.parseDouble(txtCost_alojamiento.getText()));
        seleccionado = cboEstado.getSelectedIndex();
        dts.setStrEstado((String) cboEstado.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue registrada satisfactoriamente");
                mostrar("");
                inhabilitar();

                //ocupamos la Habitación alquilada
                frmHabitacion func3 = new frmHabitacion();
                vHabitacion dts3 = new vHabitacion();

                //dts3.setIntIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                //func3.ocupar(dts3);
            }

        } else if (accion.equals("editar")) {
            dts.setIntIdReserva(Integer.parseInt(txtIdReserva.getText()));
            dts.setIntIdTrabajador(Integer.parseInt(txtIdTrabajador.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue Editada satisfactoriamente");
                mostrar("");
                inhabilitar();
                //Ocupar la habitacion alquilada
                fHabitacion func3 = new fHabitacion();
                vHabitacion dts3 = new vHabitacion();

                dts3.setIntIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                func3.ocupar(dts3);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

