    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        ObjCaj.setCedula(this.jTextCedula.getText());
        ObjCaj.setApellidos(this.jTextApellido.getText());
        ObjCaj.setNombres(this.jTextNombres.getText());
        ObjCaj.setCaja(this.jComboBox1.getSelectedIndex());
        if (ObjLogCaj.InsertarCajero(ObjCaj)){
            JOptionPane.showMessageDialog(rootPane, "Cajero Insertado Correctamente");
            this.jTextNombres.setEnabled(false);
            this.jTextApellido.setEnabled(false);
            this.jComboBox1.setEnabled(false);
            this.jButton2.setVisible(false);
            this.jButton4.setVisible(false);
            this.jButton3.setVisible(true);
        }else
            JOptionPane.showMessageDialog(rootPane, "Error al Insertar");
    }//GEN-LAST:event_jButton4ActionPerformed

