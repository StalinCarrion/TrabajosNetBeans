    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.BuscarCajero()){
            //int dialogButton = JOptionPane.YES_NO_OPTION;
            int x = JOptionPane.showConfirmDialog (null, "Cedula No encontrada. Desea crear un Cliente","Cajero no encontrado",JOptionPane.YES_NO_OPTION);
            if (x == JOptionPane.YES_OPTION){
                this.jTextNombres.setEnabled(true);
                this.jTextApellido.setEnabled(true);
                this.jComboBox1.setEnabled(true);
                this.jButton2.setVisible(true);
                this.jButton4.setVisible(true);
                this.jButton3.setVisible(false);   
            }
            else
                System.out.println("NADA");
        }
        else{
            this.jTextNombres.setText(ObjCaj.getNombres());
            this.jTextApellido.setText(ObjCaj.getApellidos());
            this.jComboBox1.setSelectedIndex(ObjCaj.getCaja());
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed

