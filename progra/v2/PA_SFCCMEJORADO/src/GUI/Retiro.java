    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:.
        String nC = objManPersonaCuenta.BuscarPersonaCuentaCedula(ArrayPersonaCuentas, this.jTextCedula.getText());

        objPersonaCuenta = objManPersonaCuenta.BuscarPersonaCedula2(ArrayPersonaCuentas, this.jTextCedula.getText());
        if (nC.equals("1")) {
            this.jTextRetirar.setEnabled(true);
            this.jTCedula.setEnabled(true);
            String cuenta = "";

            this.jTextNombreCuenta.setText(String.valueOf(objPersonaCuenta.getNombre()));
            this.jTextApellidoCuenta.setText(String.valueOf(objPersonaCuenta.getApellido()));

            this.jTextSaldoCuenta.setText(String.valueOf(objPersonaCuenta.getSaldo()));
            this.jTextNCuenta.setText(String.valueOf(objPersonaCuenta.getnCuenta()));

            if (objPersonaCuenta.getActivo().equals("1")) {
                cuenta = "corriente";
            }
            if (objPersonaCuenta.getActivo().equals("0")) {
                cuenta = "ahorros";
            }

            this.jTextTipoCuenta.setText(cuenta);

        }
        if (nC != "1") {
            JOptionPane.showMessageDialog(rootPane, "EL numero de cuenta NO es correcto");
            this.jTextApellidoCuenta.setText(null);
            this.jTextCedula.setText(null);
            this.jTextNCuenta.setText(null);
            this.jTextNombreCuenta.setText(null);
            this.jTextSaldoCuenta.setText(null);
            this.jTextTipoCuenta.setText(null);
        }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

