    private void jTextRetirarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextRetirarKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();
        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar solo numeros.");
            
        }
    }//GEN-LAST:event_jTextRetirarKeyTyped

